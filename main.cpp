
#include <iostream>
using namespace std;

int main()
{
  double length = 8;
  double width= 3;
  double area; // definition of area
  double perimeter; // definition of perimeter


  area = width * length; // computes area
  perimeter = 2 * length + 2 * width; // computes perimeter

  cout << "The area of the circle is " << area << endl;
  cout << "The perimeter of the circle is " << perimeter << endl;
  
  
}